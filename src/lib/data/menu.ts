export function getAllMenuItems() {
  return menuData.flatMap((category) => category.items);
}

export function getItemsByCategory(categoryId: string) {
  const category = menuData.find((cat) => cat.id === categoryId);
  return category?.items || [];
}

export function getMenuItemById(itemId: string) {
  return getAllMenuItems().find((item) => item.id === itemId);
}

export function getVegetarianItems() {
  return getAllMenuItems().filter((item) => item.isVegetarian);
}

export function getSpicyItems() {
  return getAllMenuItems().filter((item) => item.isSpicy);
}

